defmodule Spex do
  @moduledoc """
  `Spex` is a collection of tools for working with spectroscopy data. 

  It contains utilities for loading and working with reference spectra in `Spex.RefSpec`.

  `Spex.WebSpec` is for downloading spectrometry data.

  `Spex.Utils` provides various helpers for data wrangling and analysis

  `Spex.Regression` implements linear regression for mapping spectrometry data to known compounds.

  `Spex.Interp` provides functions to re-map data series with sample periods.

  """
end
